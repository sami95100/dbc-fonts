export interface ProductColor {
  name: string;
  hex: string;
}

export interface ProductCondition {
  id: string;
  name: string;
  nameFr: string;
  description: string;
  descriptionFr: string;
  price: number | null;
  available: boolean;
  isBestSeller?: boolean;
}

export interface ProductStorage {
  value: string;
  available: boolean;
}

export interface ProductSim {
  value: string;
  available: boolean;
  description?: string;
  descriptionFr?: string;
}

export interface Product {
  id: string;
  slug: string;
  name: string;
  brand: string;
  brandSlug: string;
  category: string;
  priceFrom: number;
  priceNew: number;
  rating: number;
  reviewCount: number;
  colors: ProductColor[];
  storages: ProductStorage[];
  sims?: ProductSim[];
  conditions: ProductCondition[];
  batteryOptions: {
    standard: { price: number };
    new: { price: number };
  };
  imageFolder: string;
  inStock: boolean;
  // Image primaire depuis l'API (couleur par defaut)
  primaryImageUrl?: string | null;
  description?: string;
  isPromo?: boolean;
}

// Helper to get image path - priorite: API primary_image_url, puis fallback local
export function getProductImage(product: Product): string {
  // Utiliser l'image primaire de l'API si disponible
  if (product.primaryImageUrl) {
    return product.primaryImageUrl;
  }
  // Fallback sur l'image locale
  return `/images/products/apple/${product.slug}/image.png`;
}

